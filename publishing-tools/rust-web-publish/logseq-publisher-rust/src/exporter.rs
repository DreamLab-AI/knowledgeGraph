fn escape_html(s: &str) -> String {
    s.chars()
        .map(|c| match c {
            '<' => "&lt;".to_string(),
            '>' => "&gt;".to_string(),
            '&' => "&amp;".to_string(),
            '"' => "&quot;".to_string(),
            '\'' => "&#x27;".to_string(),
            _ => c.to_string(),
        })
        .collect()
}

/// Export a single page to HTML (for CLI usage)
pub fn export_page_to_html(page: &Page, backlinks: &[String], config: &ExportConfig) -> String {
    let mut html = String::new();

    html.push_str(&format!("<h1>{}</h1>\n", escape_html(&page.title)));

    if !page.tags.is_empty() {
        html.push_str("<div class=\"tags\">\n");
        for tag in &page.tags {
            html.push_str(&format!("<span class=\"tag\">#{}</span>", escape_html(tag)));
        }
        html.push_str("</div>\n");
    }

    if !page.properties.is_empty() {
        html.push_str("<div class=\"properties\">\n<h3>Properties</h3>\n<ul>\n");
        for (key, value) in &page.properties {
            html.push_str(&format!(
                "<li><strong>{}:</strong> {}</li>\n",
                escape_html(key),
                escape_html(value)
            ));
        }
        html.push_str("</ul>\n</div>\n");
    }

    html.push_str("<div class=\"blocks\">\n");
    for block in &page.blocks {
        html.push_str(&render_block_html(block));
    }
    html.push_str("</div>\n");

    if config.include_backlinks && !backlinks.is_empty() {
        html.push_str("<div class=\"backlinks\">\n<h2>Backlinks</h2>\n<ul>\n");
        for backlink in backlinks {
            html.push_str(&format!(
                "<li><a href=\"#\">{}</a></li>\n",
                escape_html(backlink)
            ));
        }
        html.push_str("</ul>\n</div>\n");
    }

    html
}

fn render_block_html(block: &Block) -> String {
    let mut html = format!(
        "<div class=\"block\" data-id=\"{}\">\n<div class=\"block-content\">{}</div>\n",
        escape_html(&block.id),
        escape_html(&block.content)
    );

    if !block.children.is_empty() {
        html.push_str("<div class=\"block-children\">\n");
        for child in &block.children {
            html.push_str(&render_block_html(child));
        }
        html.push_str("</div>\n");
    }

    html.push_str("</div>\n");
    html
}

