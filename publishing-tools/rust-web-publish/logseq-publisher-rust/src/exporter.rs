#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ExportConfig {
    pub theme: String,
    pub include_backlinks: bool,
    pub include_graph_view: bool,
    pub custom_css: Option<String>,
}

impl Default for ExportConfig {
    fn default() -> Self {
        Self {
            theme: "light".to_string(),
            include_backlinks: true,
            include_graph_view: false,
            custom_css: None,
        }
    }
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PageMetadata {
    pub slug: String,
    pub title: String,
    pub properties: HashMap<String, String>,
    pub tags: Vec<String>,
    pub links: Vec<String>,
    pub backlinks: Vec<String>,
    pub content_url: String,
    pub block_count: usize,
}

#[derive(Debug, Serialize, Deserialize)]
pub struct PageContent {
    pub slug: String,
    pub title: String,
    pub blocks: Vec<Block>,
}

/// Extract metadata from a page
pub fn extract_metadata(page: &Page, backlinks: &[String]) -> PageMetadata {
    let slug = page.title.to_lowercase().replace(' ', "-");
    let block_count = crate::parser::count_blocks(&page.blocks);

    PageMetadata {
        slug: slug.clone(),
        title: page.title.clone(),
        properties: page.properties.clone(),
        tags: page.tags.clone(),
        links: page.links.clone(),
        backlinks: backlinks.to_vec(),
        content_url: format!("content/{}.json", slug),
        block_count,
    }
}

/// Export graph to HTML (for WASM compatibility)
pub fn export_to_html(graph: &Graph, config: &ExportConfig) -> Result<String, String> {
    let metadata: Vec<_> = graph
        .pages()
        .map(|page| {
            let backlinks = graph.get_backlinks(&page.path);
            extract_metadata(page, &backlinks)
        })
        .collect();

    generate_index_html(&metadata, config)
}

/// Generate index.html with navigation and search
pub fn generate_index_html(metadata: &[PageMetadata], _config: &ExportConfig) -> Result<String, String> {
    let mut html = String::from("<!DOCTYPE html>\n<html lang=\"en\">\n<head>\n");
    html.push_str("<meta charset=\"UTF-8\">\n");
    html.push_str("<meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0\">\n");
    html.push_str("<title>Logseq Published Graph</title>\n");
    html.push_str("<link rel=\"stylesheet\" href=\"style.css\">\n");
    html.push_str("</head>\n<body>\n");

    html.push_str("<div class=\"container\">\n");
    html.push_str("<header>\n");
    html.push_str("<h1>📚 Logseq Knowledge Graph</h1>\n");
    html.push_str(&format!("<p class=\"stats\">{} pages published</p>\n", metadata.len()));
    html.push_str("</header>\n");

    // Search box
    html.push_str("<div class=\"search-box\">\n");
    html.push_str("<input type=\"text\" id=\"search\" placeholder=\"Search pages...\" />\n");
    html.push_str("</div>\n");

    html.push_str("<main>\n");
    html.push_str("<div id=\"page-list\">\n");

    let mut sorted_metadata = metadata.to_vec();
    sorted_metadata.sort_by(|a, b| a.title.cmp(&b.title));

    for meta in sorted_metadata {
        html.push_str(&format!(
            "<div class=\"page-card\" data-slug=\"{}\" data-tags=\"{}\">\n",
            escape_html(&meta.slug),
            escape_html(&meta.tags.join(" "))
        ));
        html.push_str(&format!("<h3>{}</h3>\n", escape_html(&meta.title)));

        if !meta.tags.is_empty() {
            html.push_str("<div class=\"tags\">\n");
            for tag in &meta.tags {
                html.push_str(&format!("<span class=\"tag\">#{}</span>", escape_html(tag)));
            }
            html.push_str("</div>\n");
        }

        html.push_str(&format!(
            "<p class=\"meta\">{} blocks • {} links • {} backlinks</p>\n",
            meta.block_count, meta.links.len(), meta.backlinks.len()
        ));
        html.push_str("</div>\n");
    }

    html.push_str("</div>\n");
    html.push_str("<div id=\"page-content\" style=\"display:none;\"></div>\n");
    html.push_str("</main>\n");

    html.push_str("<footer>\n");
    html.push_str("<p>Generated by Logseq Publisher (Rust) • Built with ❤️ and 🦀</p>\n");
    html.push_str("</footer>\n");
    html.push_str("</div>\n");

    // Embed metadata JSON
    html.push_str("<script id=\"pages-data\" type=\"application/json\">\n");
    html.push_str(&serde_json::to_string(metadata).map_err(|e| e.to_string())?);
    html.push_str("\n</script>\n");

    html.push_str("<script src=\"app.js\"></script>\n");
    html.push_str("</body>\n</html>");

    Ok(html)
}

/// Export page content as JSON
pub fn export_page_content(page: &Page, output_dir: &Path) -> Result<(), String> {
    let slug = page.title.to_lowercase().replace(' ', "-");
    let content = PageContent {
        slug: slug.clone(),
        title: page.title.clone(),
        blocks: page.blocks.clone(),
    };

    let content_dir = output_dir.join("content");
    fs::create_dir_all(&content_dir).map_err(|e| format!("Failed to create content dir: {}", e))?;

    let file_path = content_dir.join(format!("{}.json", slug));
    let json = serde_json::to_string_pretty(&content).map_err(|e| e.to_string())?;

    fs::write(file_path, json).map_err(|e| format!("Failed to write content file: {}", e))?;

    Ok(())
}

/// Generate search index JSON
pub fn generate_search_index(metadata: &[PageMetadata]) -> String {
    let mut index = Vec::new();

    for meta in metadata {
        index.push(serde_json::json!({
            "slug": meta.slug,
            "title": meta.title,
            "tags": meta.tags,
            "links": meta.links,
        }));
    }

    serde_json::to_string_pretty(&index).unwrap_or_else(|_| "[]".to_string())
}

